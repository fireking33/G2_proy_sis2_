    private void registrarEmpleado() {
        
        int telefono = Integer.parseInt(jTextField4.getText()); // Convertir a número
        
        // Verificar si la conexión fue exitosa
        if (con != null) {
            try {
                // Consulta SQL para insertar los datos en la tabla empleado
                String sql = "INSERT INTO empleado (id_empleado, nombre_empleado, apellido_empleado, correo_electronico, telefono, usuario, contrasena, cargo) "
                        + "VALUES (DEFAULT, ?, ?, ?, ?, ?, ?, ?)";

                // Preparar la consulta
                PreparedStatement statement = con.prepareStatement(sql);
                statement.setString(1, jTextField1.getText()); // Nombre del empleado
                statement.setString(2, jTextField2.getText()); // Apellido del empleado
                statement.setString(3, jTextField3.getText()); // Correo electrónico
                statement.setInt(4, telefono); // Teléfono (ahora convertido a int)
                statement.setString(5, jTextField5.getText()); // Usuario
                statement.setString(6, jTextField6.getText()); // Contraseña
                statement.setString(7, jTextField7.getText()); // Cargo

                // Ejecutar la consulta
                int rowsInserted = statement.executeUpdate();
                if (rowsInserted > 0) {
                    JOptionPane.showMessageDialog(this, "Empleado registrado correctamente");
                }

                // Cerrar la conexión
                statement.close();
                con.close();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al registrar empleado: " + ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo conectar a la base de datos");
        }
    }
    

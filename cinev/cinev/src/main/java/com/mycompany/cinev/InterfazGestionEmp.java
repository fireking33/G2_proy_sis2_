    private void cargarCajeros() {
        // Limpiar el JComboBox antes de cargar nuevos datos
        jComboBox1.removeAllItems();

        // Consulta SQL para obtener los nombres y apellidos de los empleados que son cajeros
        String sql = "SELECT nombre_empleado, apellido_empleado FROM empleado WHERE cargo = 'Cajero'";

        try (PreparedStatement pst = con.prepareStatement(sql);
             ResultSet rs = pst.executeQuery()) {

            // Verificar si hay resultados y agregarlos al JComboBox
            boolean hayDatos = false;
            while (rs.next()) {
                String nombre = rs.getString("nombre_empleado");
                String apellido = rs.getString("apellido_empleado");
                jComboBox1.addItem(nombre + " " + apellido);
                hayDatos = true;
            }

            // Si no hay cajeros, mostrar un mensaje predeterminado
            if (!hayDatos) {
                jComboBox1.addItem("No hay cajeros disponibles");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar cajeros: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    

    private void verificarCredencialesAdmin() {
        String passwordIngresada = new String(passTxt.getPassword()); // Obtener la contraseña ingresada
        String sql = "SELECT * FROM empleado WHERE contrasena = ?"; // Consulta SQL

        try {
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, passwordIngresada);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                // Si hay un resultado, la contraseña es correcta
                JOptionPane.showMessageDialog(this, "Inicio de sesión exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                new InterfazAdmin().setVisible(true); // Abre la interfaz de administrador
                this.dispose(); // Cierra la ventana actual
            } else {
                // Si no hay resultados, la contraseña es incorrecta
                JOptionPane.showMessageDialog(this, "Contraseña incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }


  

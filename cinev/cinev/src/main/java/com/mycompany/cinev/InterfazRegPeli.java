 public  boolean existePelicula(String nombrePelicula, Connection conexion) {
        String consulta = "SELECT COUNT(*) FROM pelicula WHERE nombre_pelicula = ?";
        
        try (PreparedStatement ps = conexion.prepareStatement(consulta)) {
            ps.setString(1, nombrePelicula);
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()) {
                return rs.getInt(1) > 0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        return false;
    }

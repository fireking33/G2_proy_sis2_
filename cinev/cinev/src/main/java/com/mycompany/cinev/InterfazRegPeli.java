      public static  boolean verificarAnio(String fecha) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate fechaIngresada = LocalDate.parse(fecha, formatter);
        int añoActual = Year.now().getValue();
        
        return fechaIngresada.getYear() > añoActual;
    }
// Método para guardar los datos en la base de datos
private void guardarEnBD() {
    String sql = "INSERT INTO pelicula (nombre_pelicula, duracion, fecha_estreno, genero, clasificacion, formato, imagen) VALUES (?, ?, ?, ?, ?, ?, ?)";

    try {
        if (con == null || con.isClosed()) {
            JOptionPane.showMessageDialog(null, "Error: No se pudo establecer la conexión con la base de datos.");
            return;
        }

        // Conversión de fecha (de String a java.sql.Date)
        java.sql.Date fechaSQL;
        try {
            fechaSQL = java.sql.Date.valueOf(fechaEs.getText().trim()); // Debe estar en formato YYYY-MM-DD
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Error: El formato de la fecha debe ser YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtener los valores de los campos
        String tituloPelicula = titulo.getText().trim();
        int duracionNum = Integer.parseInt(duracion.getText().trim());
        String generoPelicula = genero.getText().trim();
        String clasificacionSeleccionada = (String) clasif.getSelectedItem();
        String formatoSeleccionado = (String) formato.getSelectedItem(); // Obtener el formato del JComboBox

        // Validación de formato (para evitar errores si no está seleccionado)
        if (formatoSeleccionado == null || formatoSeleccionado.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un formato válido.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Conversión de imagen a byte[]
        byte[] imagenBytes = null;
        try {
            if (paraFoto.getIcon() instanceof ImageIcon) {
                ImageIcon icon = (ImageIcon) paraFoto.getIcon();
                if (rutaImagen == null || rutaImagen.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Debe seleccionar una imagen.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                File imagenFile = new File(rutaImagen);
                if (!imagenFile.exists()) {
                    JOptionPane.showMessageDialog(null, "La imagen seleccionada no existe.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                InputStream is = new FileInputStream(imagenFile);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                byte[] buffer = new byte[1024];
                int bytesRead;
                while ((bytesRead = is.read(buffer)) != -1) {
                    baos.write(buffer, 0, bytesRead);
                }
                imagenBytes = baos.toByteArray();
                is.close();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo obtener la imagen.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al leer la imagen: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Preparar la consulta
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, tituloPelicula);
        pst.setInt(2, duracionNum);
        pst.setDate(3, fechaSQL);
        pst.setString(4, generoPelicula);
        pst.setString(5, clasificacionSeleccionada);
        pst.setString(6, formatoSeleccionado);
        pst.setBytes(7, imagenBytes);

        // Ejecutar la consulta
        int filasInsertadas = pst.executeUpdate();

        // Verificar si la inserción fue exitosa
        if (filasInsertadas > 0) {
            JOptionPane.showMessageDialog(null, "Película guardada exitosamente.");
        } else {
            JOptionPane.showMessageDialog(null, "Error al guardar la película.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Cerrar conexión
        pst.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al insertar en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}




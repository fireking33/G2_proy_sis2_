      public static boolean esTextoNumerico(String texto) {
        // Si el texto es nulo o está vacío, no es numérico.
        if (texto == null || texto.equals("")) {
            return false;
        }

        // Recorremos cada carácter del texto.
        for (int i = 0; i < texto.length(); i++) {
            char c = texto.charAt(i); // Obtenemos el carácter en la posición actual.
            if (c < '0' || c > '9') { // Verificamos si no es un número.
                return false; // Si no es un número, devolvemos false.
            }
        }

        // Si todos los caracteres son números, devolvemos true.
        return true;
    }
      private void buscarImagen() {
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Imagenes (JPG, PNG, GIF)", "jpg", "png", "gif");
        fileChooser.setFileFilter(filter);

        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();
            rutaImagen = file.getAbsolutePath();
            ImageIcon icon = new ImageIcon(new ImageIcon(rutaImagen).getImage().getScaledInstance(paraFoto.getWidth(), paraFoto.getHeight(), Image.SCALE_SMOOTH));
            paraFoto.setIcon(icon);
        }
    }
    public void actualizarDatosPelicula(JComboBox<String> pelisComboBox) {
        // Obtener el nombre de la película seleccionada desde InterfazGestionPelis
        String nombreSeleccionado = (String) pelisComboBox.getSelectedItem();

        if (nombreSeleccionado == null || nombreSeleccionado.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Seleccione una película para actualizar.");
            return;
        }

        // Obtener los datos desde los JTextField y JLabel
        String nuevoTitulo = titulo.getText().trim();
        String nuevaFechaEstreno = fechaEs.getText().trim();
        String nuevoGenero = genero.getText().trim();
        String nuevaDuracion = duracion.getText().trim();
        String nuevaClasificacion = clasif.getText().trim();

        // Validaciones personalizadas
        boolean tituloValido = nuevoTitulo.matches("^[A-Za-zÁÉÍÓÚáéíóúÑñ ]+$");
        boolean generoValido = nuevoGenero.matches("^[A-Za-zÁÉÍÓÚáéíóúÑñ ]+$");
        boolean duracionValida = nuevaDuracion.matches("^[0-9]+$");

        int duracionInt = 0;
        if (duracionValida) {
            duracionInt = Integer.parseInt(nuevaDuracion);
            duracionValida = (duracionInt >= 10 && duracionInt <= 499);
        }

        // Acumulador de errores para mostrar en el mensaje de advertencia
        String mensajeError = "";

        if (nuevoTitulo.isEmpty() || nuevaFechaEstreno.isEmpty() || nuevoGenero.isEmpty() ||
            nuevaDuracion.isEmpty() || nuevaClasificacion.isEmpty()) {
            mensajeError += "- Complete todos los campos antes de actualizar.\n";
        }
        if (!tituloValido) {
            mensajeError += "- El título solo puede contener letras y espacios.\n";
        }
        if (!duracionValida) {
            mensajeError += "- La duración debe ser un número entero entre 10 y 499 minutos.\n";
        }
        if (!generoValido) {
            mensajeError += "- El género solo puede contener letras y espacios.\n";
        }

        // Si hay errores, mostrar mensaje y salir del método
        if (!mensajeError.isEmpty()) {
            JOptionPane.showMessageDialog(null, mensajeError, "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar formato de fecha
        java.sql.Date sqlFechaEstreno;
        try {
            sqlFechaEstreno = java.sql.Date.valueOf(nuevaFechaEstreno);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "El formato de la fecha debe ser YYYY-MM-DD.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtener la imagen
        byte[] imagenBytes = null;
        try {
            if (paraFoto.getIcon() instanceof ImageIcon) {
                ImageIcon icon = (ImageIcon) paraFoto.getIcon();

                if (rutaImagen == null || rutaImagen.isEmpty()) {
                    JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguna imagen.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                File imagenFile = new File(rutaImagen);
                if (!imagenFile.exists()) {
                    JOptionPane.showMessageDialog(null, "El archivo de imagen no existe en la ruta seleccionada.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                InputStream is = new FileInputStream(imagenFile);
                ByteArrayOutputStream baos = new ByteArrayOutputStream();
                byte[] buffer = new byte[1024];
                int bytesRead;

                while ((bytesRead = is.read(buffer)) != -1) {
                    baos.write(buffer, 0, bytesRead);
                }

                imagenBytes = baos.toByteArray();
                is.close();
            } else {
                JOptionPane.showMessageDialog(null, "No se pudo obtener la imagen.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al leer la imagen: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Actualizar en la base de datos
        String sql = "UPDATE pelicula SET nombre_pelicula = ?, fecha_estreno = ?, genero = ?, duracion = ?, clasificacion = ?, imagen = ? WHERE nombre_pelicula = ?";

        try {
            if (con == null || con.isClosed()) {
                JOptionPane.showMessageDialog(null, "Error: No se pudo establecer la conexión con la base de datos.");
                return;
            }

            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, nuevoTitulo);
            pst.setDate(2, sqlFechaEstreno);
            pst.setString(3, nuevoGenero);
            pst.setInt(4, duracionInt);
            pst.setString(5, nuevaClasificacion);
            pst.setBytes(6, imagenBytes);
            pst.setString(7, nombreSeleccionado);

            int filasActualizadas = pst.executeUpdate();

            if (filasActualizadas > 0) {
                JOptionPane.showMessageDialog(null, "Película actualizada correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró la película a actualizar.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar la película: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


